func recursion(from target: Int) -> Int{
    print("Calculating fibonacci(\(target))...")
    if target <= 2{
        print("Terminal case, returning 1.")
        return 1
    }
    else{
        let calculate = recursion(from: target - 1) + recursion(from: target - 2)
        print("Non-terminal case, returning \(calculate).")
        return calculate
    }
}

let result = recursion(from: n)
print("The final result is: \(result).")
